# 프로그램을 사용할 때 일어나는 오류들
# - 예상 가능한 예외
# - 예상 불가능한 예외

# 예상 가능한 예외
# - 발생 여부를 사전에 인지할 수 있는 예외
# - 사용자의 잘못된 입력, 파일 호출했는데 파일 없음
# - 개발자가 반드시 명시적으로 정의해야

# 예상 불가능한 예외
# - 인터프리터 과정에서 발생하는 예외, 개발자의 실수
# - 리스트의 범위를 넘어가는 값 호출, 정수 0으로 나눔
# - 수행 불가시 인터프리터가 자동 호출

# Exception Handling : 프로그램 제품은 모든 잘못된 상황에 대한 대처가 필요하다.
